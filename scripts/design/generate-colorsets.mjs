#!/usr/bin/env node
// Generates .colorset bundles from calm-ledger-tokens.json. Idempotent.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const manifest = JSON.parse(readFileSync(join(here, "calm-ledger-tokens.json"), "utf8"));
const xcassets = join(here, "../../Packages/AtlasDesignSystem/Sources/AtlasDesignSystem/Resources/AtlasColors.xcassets");

function components(hex) {
  if (!/^#[0-9A-Fa-f]{6}([0-9A-Fa-f]{2})?$/.test(hex)) {
    throw new Error(`invalid hex "${hex}" — must be #RRGGBB or #RRGGBBAA`);
  }
  const h = hex.replace("#", "");
  const c = (i) => (parseInt(h.slice(i, i + 2), 16) / 255).toFixed(10);
  const alpha = h.length === 8 ? (parseInt(h.slice(6, 8), 16) / 255).toFixed(10) : "1.0000000000";
  return { red: c(0), green: c(2), blue: c(4), alpha };
}
function entry(appearanceValue, hex) {
  return {
    idiom: "universal",
    appearances: [{ appearance: "luminosity", value: appearanceValue }],
    color: { "color-space": "srgb", components: components(hex) },
  };
}
// Note: generator never deletes — renaming/removing a manifest token leaves an orphan colorset; reconcile manually (see M4 plan).
for (const [name, modes] of Object.entries(manifest.colors)) {
  const dir = join(xcassets, `${name}.colorset`);
  mkdirSync(dir, { recursive: true });
  const json = {
    colors: [entry("light", modes.light), entry("dark", modes.dark)],
    info: { author: "xcode", version: 1 },
  };
  writeFileSync(join(dir, "Contents.json"), JSON.stringify(json, null, 2) + "\n");
  console.log(`wrote ${name}.colorset`);
}
// Also emit a Swift fallback table for SwiftPM CLI runs (no actool → NSColor(named:) is nil).
function swiftRGBA(hex) {
  const h = hex.replace("#", "");
  const c = (i) => (parseInt(h.slice(i, i + 2), 16) / 255).toFixed(10);
  const a = h.length === 8 ? (parseInt(h.slice(6, 8), 16) / 255).toFixed(10) : "1.0000000000";
  return `RGBA(r: ${c(0)}, g: ${c(2)}, b: ${c(4)}, a: ${a})`;
}
const entries = Object.entries(manifest.colors)
  .map(([name, m]) => `        "${name}": (light: ${swiftRGBA(m.light)}, dark: ${swiftRGBA(m.dark)}),`)
  .join("\n");
const swiftFile = `// GENERATED by scripts/design/generate-colorsets.mjs — DO NOT EDIT.
// Source of truth: scripts/design/calm-ledger-tokens.json
// Purpose: SwiftPM CLI (swift run / swift test) does not compile .xcassets via actool,
// so NSColor(named:) resolves nil there. atlasColor(_:) falls back to this table.
// Release builds (xcodebuild) resolve the compiled catalog and never consult this file.

import CoreGraphics

enum AtlasColorFallback {
    struct RGBA {
        let r: CGFloat
        let g: CGFloat
        let b: CGFloat
        let a: CGFloat
    }

    static let table: [String: (light: RGBA, dark: RGBA)] = [
${entries}
    ]
}
`;
writeFileSync(join(here, "../../Packages/AtlasDesignSystem/Sources/AtlasDesignSystem/AtlasColorFallback.swift"), swiftFile);
console.log("wrote AtlasColorFallback.swift");
console.log(`\n${Object.keys(manifest.colors).length} colorsets generated`);
